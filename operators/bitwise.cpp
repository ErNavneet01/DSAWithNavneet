#include<iostream>
using namespace std;
int main(){
    // & operator
    int ans=2&3;
    cout<<ans<<endl;

    // | or operator
    int abc=2|3;
    cout<<abc<<endl;

    // ^ xor
    int cde=2^3;
    cout<<cde<<endl;

    // ~ 
    int f=~10;
    cout<<f<<endl;

    // << left shift
    int n=2<<3;
    cout<<n<<endl;

    // >>right shift
    int d=18>>3;
    cout<<d;

    return 0;
}